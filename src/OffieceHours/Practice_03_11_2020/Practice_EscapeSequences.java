package OffieceHours.Practice_03_11_2020;

public class Practice_EscapeSequences {
    public static void main(String[] args) {

        System.out.println("   Java is a programming language");


        System.out.println("\tJava is a programming language");
        // \t is used for giving paragraph

        System.out.println("\nWe love learning Java");
        // \n is used for starting a new line

        System.out.println("\n\tWe love learning Java");

        System.out.println("I love the TV show \"The Waking Dead\"");

        System.out.println("My favorite qoute is \"Night is dark,Full of terrors\"");

        // \"used for printing double qout


        System.out.println("/\\");

        // \\ is used for printing double back slash







    }



}
