package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\t\t\tHello");
        System.out.println("Hello");
        // \t means a pragrph space

        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println("\nmy\nname\nis\nparizat");

        // \n means new line, starts with new line

         //print: My favorite TV show is "Game of Thrones"

        System.out.println("My Favorite TV show is \"Game of the Throne\" ");
        System.out.println("My Favorite Book is \"Harry Potter\"");

        // \"means doubles quote to the console

        System.out.println("\\");

        // prints single slash on console

        System.out.println("/");

        System.out.println("my favorite book is 'Java'");
        System.out.println("my favorite book is \'Jave\'");

        // \' prints the' or ' prints ' as well










    }




}
