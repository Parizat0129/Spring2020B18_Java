package day17_WhileLoops;

public class WhileLoopPractice3 {
    public static void main(String[] args) {
        for (int i=0;i<100;i++){
            System.out.print("*");
        }
        System.out.println();
        int i=0;
        while(i<100){
            System.out.print("*");
            i++;
        }

        System.out.println();

        System.out.println("***");
        int number=0;
        while(number<3){//0, 1, 2
            System.out.println("* *");
            number++;
        }
        System.out.println("***");



















    }
}
